public class UserInfo {
    /**
     * staff_name : ?*
     * staff_phone : 12345678900
     * staff_job : ???
     * staff_power : 1
     */

    private String staff_name;
    private String staff_phone;
    private String staff_job;
    private String staff_power;

    public String getStaff_name() {
        return staff_name;
    }

    public void setStaff_name(String staff_name) {
        this.staff_name = staff_name;
    }

    public String getStaff_phone() {
        return staff_phone;
    }

    public void setStaff_phone(String staff_phone) {
        this.staff_phone = staff_phone;
    }

    public String getStaff_job() {
        return staff_job;
    }

    public void setStaff_job(String staff_job) {
        this.staff_job = staff_job;
    }

    public String getStaff_power() {
        return staff_power;
    }

    public void setStaff_power(String staff_power) {
        this.staff_power = staff_power;
    }
}
